package Races;

public class Race {
	protected String CompetenceBonus;
	protected String CompetenceMalus;
	protected int BonusCompetence;
	protected int MalusCompetence;
	
	public String getCompetenceBonus() {
		return CompetenceBonus;
	}
	
	public String getCompetenceMalus() {
		return CompetenceMalus;
	}
	
	public int getBonusCompetence() {
		return BonusCompetence;
	}
	
	public int getMalusCompetence() {
		return MalusCompetence;
	}
}
